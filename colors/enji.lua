-- colors/enji.lua
-- Entry point for `:colorscheme enji`.
--
-- 臙脂 (enji-iro) — a traditional Japanese cochineal-red pigment name.
-- This theme's primary accent landed within a hair of the real enji-iro
-- hex value, so the whole palette was renamed to its closest traditional
-- Japanese color names: enji (red), yamabuki (gold), mizu/asagi (blues),
-- fuji/murasaki (purples), yanagi (sage), rikyū (muted grey), shironeri
-- (ivory foreground).
--
-- For transparency control, prefer calling setup() from your config instead:
--
--   require("dentoushoku.enji").setup({ transparent = true })

require("dentoushoku.enji").setup({ transparent = true })
